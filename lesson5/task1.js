let averageGrade;
averageGrade = 70;

if (averageGrade < 60) {
	console.log('Незадовільно');
} else if (averageGrade < 71) {
	console.log('Задовільно');
} else if (averageGrade < 81) {
	console.log('Добре');
} else if (averageGrade < 91) {
	console.log('Дуже добре');
} else if (averageGrade <= 100) {
	console.log('Відмінно');
} else {
	console.log('Error');
}
